//WHILE LOOP
#include <iostream>
using namespace std;
int main() {
    int n;
    cin>>n;
    int i=1;//INITIALISATION OF i
    while(i<=n){//CONDITION CHECK

        cout <<i<<" ";//TASK
        
        //cout<<i ;  aise space print nhi hogi

        i=i+1;//UPDATION 
    }
    return 0;
}